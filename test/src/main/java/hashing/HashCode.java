package hashing;

public class HashCode {
	/*
	 * hashCode() method is used to get a unique integer for given object. This
	 * integer is used for determining the bucket location, when this object
	 * needs to be stored in some HashTable like data structure. By default,
	 * Object’s hashCode() method returns and integer representation of memory
	 * address where object is stored.
	 * 
	 * equals() method, as name suggest, is used to simply verify the equality
	 * of two objects. Default implementation simply check the object references
	 * of two objects to verify their equality.
	 * 
	 * What is Hashing
	 * 
	 * Hashing in its simplest form, is a way to assigning a unique code for any
	 * variable/object after applying any formula/algorithm on its properties. A
	 * true Hashing function must follow this rule:
	 * 
	 * Hash function should return the same hash code each and every time, when
	 * function is applied on same or equal objects. In other words, two equal
	 * objects must produce same hash code consistently.
	 */
}
